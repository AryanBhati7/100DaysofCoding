public class loop1 {
    public static void main(String args[]){
        //WHILE Loop
        int count = 1;
        while(count<=100){
            System.out.println("Hello World");
            count++;
        }
        //FOR-LOOP
        for(int i=1; i<=100; i++){
            System.out.println("Hello World");
        }
        //Do-While Loop
        int count2 = 1;
        do{
            System.out.println("Hello world");
        }while(count2<=100);
    }
}
