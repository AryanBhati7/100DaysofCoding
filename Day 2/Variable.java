public class Variable {
    public static void main(String args[]){
        byte a = 5;
        System.out.println(a);
        char ch = 'a';
        System.out.println(ch);
        boolean var = false;
        System.out.println(var);
        int number = 25;
        System.out.println(number);
        long num = 15876;
        System.out.println(num);
        double num2 = 2168;
        System.out.println(num2);
    }
}
