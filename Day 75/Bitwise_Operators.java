public class Bitwise_Operators {
    public static void main(String args[]){
        int a  = 5, b = 6;
        //AND Operator
        System.out.println(a&b);
        //OR Operator
        System.out.println(a|b);
        //XOR Operator
        System.out.println(a^b);
        //NOT Operator - Binary One's Complement
        System.out.println(~a + "  " + ~b);
        //Left Shift Operator
        System.out.println(a<<2);
    }
}
